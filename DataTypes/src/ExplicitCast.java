public class ExplicitCast {
    public static void main(String[] args) {
        int i = 127;
        byte b = (byte)i;
        System.out.println(b);

        int x = 97;
        char ch = (char)x;
        System.out.println(ch);



        int y = 130;
        byte z = (byte)y;
        System.out.println(z);
    }
}
